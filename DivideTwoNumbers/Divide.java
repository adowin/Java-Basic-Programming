// Write a Java program to divide two numbers and print on the screen.

public class Divide {

    public static void main(String[] args) {

        int num1 = 4;
        int num2 = 2;
        int value = num1 / num2;

        System.out.println(num1 + " " + "/" + " " + num2 + " " + "=" + " " + value);

    }
}